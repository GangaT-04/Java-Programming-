 class ConvertDoubleIntoString {
    public static void main(String a[])
    {
        double d1=98.657;
        double d2=548.855446;
        
        String s1=Double.toString(d1);
        String s2=Double.toString(d2);
        
        System.out.println(s1);
        System.out.println(s2);
    }
}
