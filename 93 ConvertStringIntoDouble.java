
class ConvertStringIntoDouble {
    public static void main(String a[])
    {
        String s1="21.983";
        String s2="432.98364";
        
        double d1=Double.parseDouble(s1);
        double d2=Double.parseDouble(s2);
        
        System.out.println(d1);
        System.out.println(d2);
    }
}
